package Congeladora;

public class DetalleRegistro {
	public int cantidad;
	
	public DetalleRegistro(int cantidad) {
		super();
		this.cantidad = cantidad;
	}

	/**
	 * @return the cantidad
	 */
	public int getCantidad() {
		return cantidad;
	}

	/**
	 * @param cantidad the cantidad to set
	 */
	public void setCantidad(int cantidad) {
		this.cantidad = cantidad;
	}
	
	
	
	

}
